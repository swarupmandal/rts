package org.appsquad.bean;

public class UserClientMappingBean {

	private Integer resClientMappingId;
	private Integer clientId;
	private Integer userId;
	private String userName;
	private String userEmil;
	private String userID;
	
	public Integer getResClientMappingId() {
		return resClientMappingId;
	}
	public void setResClientMappingId(Integer resClientMappingId) {
		this.resClientMappingId = resClientMappingId;
	}
	public Integer getClientId() {
		return clientId;
	}
	public void setClientId(Integer clientId) {
		this.clientId = clientId;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserEmil() {
		return userEmil;
	}
	public void setUserEmil(String userEmil) {
		this.userEmil = userEmil;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
}
